package org.vlagru;

public class cc13 {

	public static void main(String[] args) {
		int x = 10;
		int y = 11;
		System.out.println(Integer.max(x, y));
		double d = x / 2.0;
		System.out.println(d);
		double dd = 11.25;
		System.out.println(Double.max(d, dd));
		long l = 100L;
		long ll = 25;
		System.out.println(Long.max(l, ll));
		System.out.println(Integer.compare(x, y));
		String money = "300";
		System.out.println(Integer.valueOf(money)); // Integer a =...
		System.out.println(Integer.parseInt(money)); // int b =...
		String o = "2";
		System.out.println(Integer.parseInt(o));
		
	}

}
